package com.imlianai.zjdoll.app.modules.support.signin.enm;

public enum AwardContentEnm {

	REDPACKET(1, "?元", "抽中了?元红包！"),
	COIN(2, "?币", "哇！抽中了?币！"),
	COMMDEBRIS(3, "?块", "哇！抽中了?块普通娃娃碎片！"),
	RAREDEBRIS(4, "?块", "哇！抽中了?块稀有娃娃碎片！");
	
	public int type;
	
	public String content;
	
	public String succDesc;

	private AwardContentEnm(int type, String content, String succDesc) {
		this.type = type;
		this.content = content;
		this.succDesc = succDesc;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getSuccDesc() {
		return succDesc;
	}

	public void setSuccDesc(String succDesc) {
		this.succDesc = succDesc;
	}
}
