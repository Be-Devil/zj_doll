package com.imlianai.zjdoll.app.modules.support.dailytask.enm;

public enum BoxRewardType {
	
	//FIRST_BOX_reward(1, 0, "奖励1~3币"),
	FIRST_BOX_reward(1, 0, "奖励3币"),
	SECOND_BOX_reward(2, 0, "奖励3~10币"),
	THIRTH_BOX_reward(3, 0, "奖励20~40币"),
	FOURTH_BOX_reward1(4, 0, "奖励100币(必得)"),
	FOURTH_BOX_reward2(4, 1, "现金红包(随机出现)"),
	FIFTH_BOX_reward1(5, 0, "奖励300币(必得)"),
	FIFTH_BOX_reward2(5, 1, "随机2.00~3.00元红包(必得)");
	
	public int boxId;
	
	public int rewardType;
	
	public String rewardDesc;
	
	BoxRewardType(int boxId, int rewardType, String rewardDesc) {
		this.boxId = boxId;
		this.rewardType = rewardType;
		this.rewardDesc = rewardDesc;
	}

	public int getBoxId() {
		return boxId;
	}

	public void setBoxId(int boxId) {
		this.boxId = boxId;
	}

	public int getRewardType() {
		return rewardType;
	}

	public void setRewardType(int rewardType) {
		this.rewardType = rewardType;
	}

	public String getRewardDesc() {
		return rewardDesc;
	}

	public void setRewardDesc(String rewardDesc) {
		this.rewardDesc = rewardDesc;
	}
}
