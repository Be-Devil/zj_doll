package com.imlianai.zjdoll.app.modules.support.event.twistedEggMachine.enm;

public enum RewardMsg {
	
	FIRST_MSG(1, "恭喜 %s 在扭蛋机扭出了 %s ，看着别人中奖的亲不去扭蛋试试吗？"),
	SECOND_MSG(2, "恭喜 %s 在扭蛋机扭出了 %s ，“任务扭蛋”开出的奖品更丰富哟~"),
	THIRD_MSG(3, "恭喜 %s 在扭蛋机扭出了 %s ，据说10连抽更容易扭出大奖哟~~");
	
	public int num;
	
	public String desc;

	private RewardMsg(int num, String desc) {
		this.num = num;
		this.desc = desc;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
