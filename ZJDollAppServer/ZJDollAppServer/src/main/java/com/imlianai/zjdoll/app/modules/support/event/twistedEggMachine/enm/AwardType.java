package com.imlianai.zjdoll.app.modules.support.event.twistedEggMachine.enm;

public enum AwardType {

	COIN(0, "金币"),
	BIGPRIZE(1, "大奖品"),
	LUCKYBAG(2, "福袋");
	
	public int type;
	
	public String desc;

	private AwardType(int type, String desc) {
		this.type = type;
		this.desc = desc;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
