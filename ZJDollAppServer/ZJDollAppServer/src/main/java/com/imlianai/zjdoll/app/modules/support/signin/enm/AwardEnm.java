package com.imlianai.zjdoll.app.modules.support.signin.enm;

public enum AwardEnm {

	COIN_2(1, 2, 2, "2币"),
	COIN_5(2, 2, 5, "5币"),
	COIN_10(3, 2, 10, "10币"),
	COIN_50(4, 2, 50, "50币"),
	COMMDEBRIS(5, 3, 0, "普通娃娃碎片"),
	RAREDEBRIS(6, 4, 0, "稀有娃娃碎片"),
	REDPACKET_1(7, 1, 0.01, "0.01元红包"),
	
	REDPACKET_2(8, 1, 1.3, "1.3元红包"),
	REDPACKET_3(9, 1, 5.0, "5.0元红包"),
	REDPACKET_4(10, 1, 8.8, "8.8元红包");
	
	private int awardType;
	
	private int type;
	
	private double num;
	
	private String desc;

	private AwardEnm(int awardType, int type, double num, String desc) {
		this.type = type;
		this.num = num;
		this.desc = desc;
	}

	public int getAwardType() {
		return awardType;
	}

	public void setAwardType(int awardType) {
		this.awardType = awardType;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public double getNum() {
		return num;
	}

	public void setNum(double num) {
		this.num = num;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
