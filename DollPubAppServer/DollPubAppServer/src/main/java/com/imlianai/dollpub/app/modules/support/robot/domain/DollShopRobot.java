package com.imlianai.dollpub.app.modules.support.robot.domain;

import com.imlianai.rpc.support.common.BaseModel;

public class DollShopRobot extends BaseModel{

	long uid;
	
	int dailyLimit;
	
	double amt;

	public DollShopRobot(){}
	
	public DollShopRobot(long uid,int dailyLimit){
		this.uid=uid;
		this.dailyLimit=dailyLimit;
	}
	public long getUid() {
		return uid;
	}

	public void setUid(long uid) {
		this.uid = uid;
	}

	public int getDailyLimit() {
		return dailyLimit;
	}

	public void setDailyLimit(int dailyLimit) {
		this.dailyLimit = dailyLimit;
	}

	public double getAmt() {
		return amt;
	}

	public void setAmt(double amt) {
		this.amt = amt;
	}

}
