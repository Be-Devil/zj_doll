package com.imlianai.dollpub.app.modules.support.exchange.vo;

import java.io.Serializable;

public class ExchangeBaseDollInfo implements Serializable{

	private static final long serialVersionUID = 1L;
	/**
	 * 娃娃id
	 */
	private int id;
	/**
	 * 娃娃名称
	 */
	private String name;
	/**
	 *	娃娃图片
	 */
	private String path;
	/**
	 * 库存
	 */
	private int inventory;
	
	/**
	 * 已兑换or已合成数量
	 */
	private int exchangedNum;
	
	/**
	 * 房间详情图片
	 */
	private String imgRoom;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getPath() {
		return path;
	}
	
	public void setPath(String path) {
		this.path = path;
	}
	
	public int getInventory() {
		return inventory;
	}
	
	public void setInventory(int inventory) {
		this.inventory = inventory;
	}

	public int getExchangedNum() {
		return exchangedNum;
	}

	public void setExchangedNum(int exchangedNum) {
		this.exchangedNum = exchangedNum;
	}

	public String getImgRoom() {
		return imgRoom;
	}

	public void setImgRoom(String imgRoom) {
		this.imgRoom = imgRoom;
	}
}
